package model;

/**
 *
 * @author ADMIN
 */
public class Category {
 private int id;
 private String name;
 private int total;

    public Category() {
    }

    public Category(int id, String name, int total) {
        this.id = id;
        this.name = name;
        this.total = total;
    }

    public int getTotal() {
        return total;
    }

    public void setTotal(int total) {
        this.total = total;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    @Override
    public String toString() {
        return "Category{" + "id=" + id + ", name=" + name + ", total=" + total + '}';
    }


 
}
